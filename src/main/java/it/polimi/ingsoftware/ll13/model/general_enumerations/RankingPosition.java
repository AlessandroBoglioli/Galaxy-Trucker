package it.polimi.ingsoftware.ll13.model.general_enumerations;

/**
 * Represents the ranking positions within the game.
 *
 * The RankingPosition enumeration defines the ordered positions
 * that can be assigned to players based on their current or final
 * ranking in the game. These positions are used to indicate
 * the relative standing of players, such as determining winners
 * or ranking participants.
 */
public enum RankingPosition {
    First, Second, Third, Fourth;
}
